
public class SwitchExample {

	public static void main (String[] args){
		int number =20;
		switch(number){
		case 10:System.out.println("number is 10");break;
		case 20:System.out.println("number is 20");break;
		case 30:System.out.println("number is 30");break;
		case 40:System.out.println("number is 40");break;
		default: System.out.println("No number entered");
		
		
		}
	}
}
